# for

for i in (0, 1, 2):
    print(f"{i} hello")
print()

# range сгенерирует список для вас  принимает в качестве аргумента количество
# range(start, stop[, step])
for i in range(0, 5, 2):
    print(f"{(i) * 2} hello i")

print()

#range(stop)
for i in range (5):
    print(i)

print()

x = range(3, 6)
for n in x:
    print(n)

